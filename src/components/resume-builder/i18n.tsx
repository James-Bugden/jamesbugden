import { createContext, useContext } from "react";

export type Lang = "en" | "zh-tw";

export const ResumeBuilderLangContext = createContext<Lang>("en");

export function useResumeBuilderLang(): Lang {
  return useContext(ResumeBuilderLangContext);
}

export function useT() {
  const lang = useResumeBuilderLang();
  return (key: keyof typeof translations.en, fallback?: string): string => {
    const val = translations[lang]?.[key] ?? translations.en[key] ?? fallback ?? key;
    return val;
  };
}


/**
 * Sprint 1 experiment variant helpers (HIR-64).
 *
 * Wraps the postgres RPCs that atomically assign onboarding/drip variants
 * to a profile or anonymous lead. Each RPC is idempotent: calling it after
 * an assignment exists returns the existing variant unchanged.
 *
 * The helpers below normalise RPC return shapes and protect against
 * accidental writes by callers that pass empty ids.
 */
import { supabase } from "@/integrations/supabase/client";

export type Variant = "control" | "treatment";

const VALID_VARIANTS: ReadonlyArray<Variant> = ["control", "treatment"];

export function parseVariant(value: unknown): Variant | null {
  if (typeof value !== "string") return null;
  return (VALID_VARIANTS as ReadonlyArray<string>).includes(value)
    ? (value as Variant)
    : null;
}

async function callVariantRpc(
  fnName: string,
  args: Record<string, unknown>,
): Promise<Variant | null> {
  try {
    // Cast through `any` because the auto-generated types.ts (regenerated by
    // Lovable post-deploy) does not yet include the HIR-64 RPCs.
    const { data, error } = await (supabase.rpc as unknown as (
      fn: string,
      args?: Record<string, unknown>,
    ) => Promise<{ data: unknown; error: { message: string } | null }>)(
      fnName,
      args,
    );
    if (error) {
      if (import.meta.env.DEV) {
        console.warn(`[experiments] ${fnName} failed:`, error.message);
      }
      return null;
    }
    return parseVariant(data);
  } catch (err) {
    if (import.meta.env.DEV) {
      const msg = err instanceof Error ? err.message : String(err);
      console.warn(`[experiments] ${fnName} threw:`, msg);
    }
    return null;
  }
}

export async function assignOnboardingVariant(
  userId: string | null | undefined,
): Promise<Variant | null> {
  if (!userId) return null;
  return callVariantRpc("assign_onboarding_variant", { p_user_id: userId });
}

export async function assignDripVariantForLead(
  leadId: string | null | undefined,
): Promise<Variant | null> {
  if (!leadId) return null;
  return callVariantRpc("assign_drip_variant_for_lead", { p_lead_id: leadId });
}

export async function assignDripVariantForUser(
  userId: string | null | undefined,
  email: string | null | undefined,
): Promise<Variant | null> {
  if (!userId || !email) return null;
  return callVariantRpc("assign_drip_variant_for_user", {
    p_user_id: userId,
    p_email: email,
  });
}
